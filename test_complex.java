public class TestComplex {
    public static void main(String[] args) {
        int i = 0;
        
        while (i < 3) {
            System.out.println("While: " + i);
            i++;
        }
        
        for (int j = 0; j < 2; j++) {
            System.out.println("For: " + j);
        }
    }
}