// TypeScript code to demonstrate conditional handling similar to the flowchart
let marks: number = 72;

if (marks >= 90) {
    console.log("Grade A");
} else if (marks >= 75) {
    console.log("Grade B");
} else if (marks >= 50) {
    console.log("Grade C");
} else {
    console.log("Fail");
}

console.log("End of grade checking");