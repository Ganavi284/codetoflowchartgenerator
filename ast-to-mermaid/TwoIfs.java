public class TwoIfs {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        
        if (a > 0) {
            System.out.println("a is positive");
        }
        
        if (b > 5) {
            System.out.println("b is greater than 5");
        }
        
        System.out.println("End");
    }
}