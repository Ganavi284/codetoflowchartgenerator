public class FormattingExamples {
    public static void main(String[] args) {
        // Example 1: Print statements with proper "Print: " prefix
        System.out.println("Program started");
        
        // Example 2: Input prompt without "Print: " prefix
        System.out.print("Enter your name: ");
        
        // Example 3: Variable output with "Print: " prefix
        String name = "John";
        System.out.println("Hello, " + name);
        
        // Example 4: Complex message with "Print: " prefix
        int count = 5;
        System.out.println("Processed " + count + " items successfully");
        
        // Example 5: Simple status message
        System.out.println("Operation completed");
    }
}