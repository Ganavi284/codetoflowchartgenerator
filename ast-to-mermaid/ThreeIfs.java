public class ThreeIfs {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int c = 15;
        
        if (a > 0) {
            System.out.println("a is positive");
        }
        
        if (b > 5) {
            System.out.println("b is greater than 5");
        }
        
        if (c > 10) {
            System.out.println("c is greater than 10");
        }
        
        System.out.println("End");
    }
}